						<p>Os motoristas que circulam pela BR-277, em Cascavel, no Oeste, já observam os primeiros trabalhos da obra de duplicação da rodovia, nas proximidades do trevo Cataratas. A concessionária Ecocataratas, responsável pelo trecho, começou nesta segunda-feira os serviços de limpeza e terraplanagem na rodovia próximo ao km 583, por onde teve início a obra. Os investimentos nesta primeira etapa são de 60 milhões de reais, recursos do próprio pedágio. Serão duplicados nove quilômetros, no trecho que começa 500 metros após o Trevo Cataratas, local estratégico que recebe e distribui o tráfego das principais rodovias da região até a Ferroeste. O secretário de Infraestrutura e Logística, José Richa Filho, ressaltou que a obra facilita o trafégo e o transporte para a região. // SONORA JOSÉ RICHA FILHO // Nesta primeira etapa da obra serão duplicados 3,2 quilômetros. Está prevista a construção de duas trincheiras, implantação de balança de pesagem e de um posto da Polícia Rodoviária Federal, além da construção de vias marginais em todo o trecho no sentido Cascavel para Guarapuava. Todo o projeto foi analisado e aprovado pelo DER, o Departamento de Estradas de Rodagem do Paraná, além de ser revisado pela Agência Reguladora de Serviços Públicos Delegados de Infraestrutura do Paraná. A previsão é que os quilômetros restantes sejam feitos no início do ano que vem. Neste mês também está previsto o início da duplicação de mais dois quilômetros da rodovia, no perímetro urbano de Guarapuava, na Região Central. A previsão da concessionária Ecocataratas é que os trabalhos comecem em 15 dias. Serão duplicados dois trechos, do km 344 ao km 345,2 e do km 349,3 até o 350,2. As obras também contam com a construção de uma trincheira no acesso à PR-170. Além disso, serão implantadas vias marginais. O investimento é de 25 milhões de reais. Estas duas obras de duplicação, inicialmente, não estavam previstas no contrato de concessão. Após muito diálogo entre o Governo do Paraná e concessionária elas foram incluídas no contrato para serem feitas ainda este ano, sem acréscimo de tarifa para os usuários. (Repórter: Gustavo Vaz).</p>

