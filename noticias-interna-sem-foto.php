						<p>A tarifa residencial da Copel terá redução de 14,3% a partir de sexta-feira (24), conforme a revisão tarifária da companhia definida nesta terça-feira (21). O índice de redução beneficia todos os consumidores, mas varia de acordo com a classe de tarifa. Na média, a tarifa da Copel vai ficar 12,87% mais barata, considerando clientes industriais, comerciais e residenciais.</p>

						<p>"Gradual e progressivamente a Copel recompõe sua condição anterior aos desatinos cometidos no setor de energia na antiga gestão do governo federal, que tantos prejuízos trouxe ao País", diz o governador Beto Richa.</p>


						<p>"A redução beneficia os usuários, sem prejuízo do cronograma de aportes da empresa, que segue investindo vigorosamente em geração, distribuição e transmissão de energia."</p>

						<blockquote>
							<p>
								Isso significa investir cada vez mais na qualidade do fornecimento de energia, aplicando os recursos de modo eficaz”
								<strong>Luiz Fernando Leone Vianna</strong>
								<span>Presidente da Copel</span>
							</p>
						</blockquote>


						<p>O presidente da Copel, Luiz Fernando Leone Vianna, afirma que a busca pela eficiência tem norteado a atuação da companhia nos últimos anos. "Isso significa investir cada vez mais na qualidade do fornecimento de energia, aplicando os recursos de modo eficaz", diz Vianna. "Reduzir a conta de luz significa contribuir para o desenvolvimento do Estado, ao baratear o uso de energia e tornar o Paraná ainda mais competitivo para atrair investimentos."</p>

						<p>A redução da tarifa da Copel foi homologada em reunião Agência Nacional de Energia Elétrica (Aneel) nesta terça (21), em Brasília, após análise da planilha detalhada da Copel que demonstra a possibilidade de reduzir a conta de luz no Estado.</p>

						<p>A diminuição foi possível graças ao aumento do nível dos reservatórios das usinas hidrelétricas. Com isso, grande parte das usinas térmicas, mais caras, foram desligadas, o que barateou o custo de produção da energia.</p>

						<p>A redução vai se refletir na vida dos paranaenses. Atualmente a média de consumo mensal de uma residência no Paraná é 166 kWh por mês. Hoje, um cliente com esse consumo paga cerca de R$ 125. Após a redução, a mesma fatura ficará em R$ 108.</p>

						<h3>Luz Fraterna</h3>

						<p>
							Além da redução na tarifa para todos os consumidores da Copel no Paraná, aqueles que atenderem os requisitos para fazer parte do Programa Luz Fraterna podem ter a conta de luz paga integralmente pelo Governo do Estado.
						</p>

						 <figure>
							<img src="_assets/images/fake/image-fake-9.jpg" alt="The Pulpit Rock" width="304" height="228">
							<figcaption>COPEL - A tarifa residencial da Copel terá redução de 14,3% a partir de sexta-feira (24), conforme a revisão tarifária da companhia definida nesta terça-feira (21). Curitiba, 21/06/2016. Foto: Jonas Oliveira/AENPR/</figcaption>
						</figure>

						<p>Para participar, é preciso atender aos seguintes requisitos: o consumo de energia elétrica em casa não pode ultrapassar 120 kWh no mês, a família deve ter renda mensal de até meio salário mínimo por pessoa, o imóvel precisa ser usado para fins residenciais em áreas urbana ou rural e o responsável pela unidade consumidora deve possuir apenas uma unidade consumidora sob sua responsabilidade.</p>

						<p>Também é preciso apresentar comprovante de renda e o Número de Identificação Social (NIS), registrado no Cadastro Único de Programas Sociais.</p>

						<p>
							A lei que regulamenta o Luz Fraterna (Lei 17.639, de 31/07/2013) prevê o benefício apenas para quem estiver inscrito no Cadastro Único. Basta reunir os documentos e ir até um posto de atendimento da Copel para fazer o cadastro. Depois, é importante manter o consumo dentro dos limites de 120 kWh por mês para ter a conta quitada.

